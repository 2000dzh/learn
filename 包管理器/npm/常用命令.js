// 设置镜像
// npm config set 镜像地址

// 查看当前镜像
// npm config get registry

// 常用命令简写说明
// -g  =>  --global (表示安装到全局)
// -S  =>  --save (表示安装的包将写入package.json里面的 dependencies 生产依赖)
// -D  =>  --save (表示安装的包将写入packege.json里面的 devDependencies 开发依赖)
//  i  =>  install (表示安装)

// 初始化项目(安装 package.json)
// npm init -y

// 安装项目的所有依赖
// npm install

// 安装包
// npm i axios (安装到 dependencies 生产依赖)
// npm i -g axios (全局安装包)
// npm i -S axios  (安装包信息将加入到 dependencies 生产依赖)
// npm i -D axios  (安装包信息将加入到 devDependencies 开发依赖)

// 删除包
// npm uninstall axios (删除指定包)
// npm uninstall -g axios (删除全局安装的包)

// 更新包
// npm update axios (更新依赖到最新版本)
// npm update axios@3.2 (更新依赖到指定版本)
// npm update -g axios (更新全局安装的包)

// 查看版本号
// npm -v

// 查看当前目录项目的模块目录地址
// npm root

// 查看全局安装的模块所在目录
// npm root -g

// 查看当前目录项目的已安装模块清单列表
// npm list | npm ls

// 清除项目中没有被使用的包
// npm prune

// 清除npm的缓存
// npm cache clean




